									<h2 class="post-title">5 financial indicators every entrepreneur should monitor</h2>
									<p>Proper financial management is crucial because it allows you to make timely, well-informed decisions in response to changing conditions.

										<br><br>Surprisingly, many entrepreneurs look at financial reports only at year-end or even a few months later when financial statements become available. That lack of attention is putting their business at risk, says Jorge Henao, a BDC Business Consultant specialized in financial management and strategy.
										
										<br><br>Review your indicators every month
										You have to be disciplined in reviewing financial data at least on a monthly basis and conducting more thorough analysis every quarter, Henao says.
										
										<br><br>You want to compare your company’s performance to objectives set out at the beginning of the year, based on a long-term strategic plan. You then make adjustments as necessary throughout the year to accomplish the objectives.</p>
									<blockquote>
										<span class="icon"><i class="fa fa-quote-left"></i></span>
										<p class="text">"You want to make decisions at the right time," he says, "If you wait until year end to address issues, it will probably be too late.</p>
										<h5 class="name">- Rosalina Pong</h5>
									</blockquote>
									<p>5 key indicators
										Henao says key financial indicators fall into these categories:
										
										<br><br>Growth—Are your sales and profits increasing or decreasing year-over-year? Is there a trend?
										Profitability—Is your business making enough profit compared to other similar companies?
										Liquidity—Can the company meet its short-term obligations?
										Leverage—Is the company taking advantage of financing to operate and grow?
										Activity—Are you managing the assets of the company effectively?
										Moreover, it’s critical for entrepreneurs to project and monitor cash flow, Henao adds. Even a company that is generating profits can quickly find itself in trouble if it doesn’t have enough cash to operate. Thus, you should know your financing needs in advance in order to manage your business proactively.
										<br><br>"If the business is growing, you are most likely going to require financing for receivables, inventory, machinery and equipment to hire more people, etc. If you wait until you need the funds, you’re putting the company in jeopardy."

										<br><br>Benchmark your financial performance
										Henao recommends that entrepreneurs benchmark the financial performance of their business against that of similar companies in the same industry. Results that are below the average may highlight areas for improvement.
										
										<br><br>For instance, a subpar gross profit margin might indicate faulty pricing based on an inaccurate reading of costs. To solve the situation, you will likely have to reduce costs, increase prices or a combination of the two.</p>
										<blockquote>
											<span class="icon"><i class="fa fa-quote-left"></i></span>
											<p class="text">"Entrepreneurs often work on intuition," Henao says. "But having the right information at the right time will help entrepreneurs make more educated decisions."</p>
										</blockquote>
